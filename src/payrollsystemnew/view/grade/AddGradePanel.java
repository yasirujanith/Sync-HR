    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if("".equals(gradeIDText.getText())||"".equals(basicSalaryText.getText())||"".equals(otRateText.getText())||"".equals(noPayRateText.getText())||"".equals(maxLoanAmountText.getText())||"".equals(vehicleAllowenceText.getText())||"".equals(transportAllowenceText.getText())){
            JOptionPane.showMessageDialog(this, "Fill out all the fields.");
        }else{
            String gradeID=gradeIDText.getText();
            double basicSalary=Double.parseDouble(basicSalaryText.getText());
            double OTRate=Double.parseDouble(otRateText.getText());
            double noPayRate=Double.parseDouble(noPayRateText.getText());
            double maxLoanAmt=Double.parseDouble(maxLoanAmountText.getText());
            double vehicleAllowance=Double.parseDouble(vehicleAllowenceText.getText());
            double transportAmt=Double.parseDouble(transportAllowenceText.getText());
            int leavesCount=Integer.parseInt(leaveCountText.getText());
            int noPayCount=Integer.parseInt(noPayCountText.getText());
            int halfDayCount=Integer.parseInt(halfDayCountText.getText());

            try {
                int rowCount = GradeController.addGrade(new Grade(gradeID, basicSalary, OTRate, noPayRate, maxLoanAmt, vehicleAllowance, transportAmt, leavesCount, noPayCount, halfDayCount));
                if(rowCount>0){
                    JOptionPane.showMessageDialog(this, "Grade addition successful.");
                }else{
                    JOptionPane.showMessageDialog(this, "Grade addition failed.");
                }
            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(this, "Grade addition failed. Grade ID exists");
            }
        }

    }//GEN-LAST:event_addButtonActionPerformed

