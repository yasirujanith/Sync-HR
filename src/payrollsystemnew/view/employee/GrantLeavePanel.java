    private void employeeIDComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_employeeIDComboItemStateChanged
        if(employeeIDCombo.getSelectedIndex()!=-1){
            try {
                Employee employee=EmployeeController.searchEmployee(employeeIDCombo.getSelectedItem().toString());
                nameText.setText(employee.getFirstName()+" "+employee.getLastName());

                Grade grade=GradeController.searchGrade(employee.getGradeID());
                SimpleDateFormat yearFormat=new SimpleDateFormat("yyyy");
                String year=yearFormat.format(dateText.getDate());
                LeaveCount leaveCount=EmployeeController.getUsedLeaveCount(employeeIDCombo.getSelectedItem().toString(), year);
                remainingLeavesText.setText(Integer.toString(grade.getLeavesCount()-leaveCount.getHalfDayCount()/2));
                remainingHalfDayText.setText(Integer.toString(grade.getHalfDayCount()-leaveCount.getHalfDayCount()));
                remainingNoPayText.setText(Integer.toString(grade.getNoPayLeaveCount()-leaveCount.getNoPayCount()));
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(GrantLeavePanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_employeeIDComboItemStateChanged

    private void doneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneButtonActionPerformed
        String employeeID=employeeIDCombo.getSelectedItem().toString();
        String leaveType=leaveTypeCombo.getSelectedItem().toString();
        Date startingDate=leaveDateText.getDate();
        int numOfDays=Integer.parseInt(numOfDaysText.getText());
        String reason=reasonText.getText();

        String leaveDate=dateFormat.format(startingDate);
        Calendar c = Calendar.getInstance();
        c.setTime(startingDate);
        int[] rowCount=new int[numOfDays];
        
            for(int i=0;i<numOfDays;i++){
                try {
                    rowCount[i]=EmployeeController.addLeaveDetail(new Leaves(employeeID, leaveDate, leaveType, reason));
                    c.add(Calendar.DATE, 1);
                    leaveDate=dateFormat.format(c.getTime());
                } catch (ClassNotFoundException | SQLException ex) {
                    Logger.getLogger(GrantLeavePanel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            int totalRowCount=0;
            for(int i=0;i<numOfDays;i++){
                totalRowCount+=rowCount[i];
            }
            if(totalRowCount==numOfDays){
            try {
                JOptionPane.showMessageDialog(this, "Leave added successfully.");
                Employee employee=EmployeeController.searchEmployee(employeeIDCombo.getSelectedItem().toString());
                Grade grade=GradeController.searchGrade(employee.getGradeID());
                SimpleDateFormat yearFormat=new SimpleDateFormat("yyyy");
                String year=yearFormat.format(dateText.getDate());
                LeaveCount leaveCount=EmployeeController.getUsedLeaveCount(employeeIDCombo.getSelectedItem().toString(), year);
                remainingLeavesText.setText(Integer.toString(grade.getLeavesCount()-leaveCount.getHalfDayCount()/2));
                remainingHalfDayText.setText(Integer.toString(grade.getHalfDayCount()-leaveCount.getHalfDayCount()));
                remainingNoPayText.setText(Integer.toString(grade.getNoPayLeaveCount()-leaveCount.getNoPayCount()));
                numOfDaysText.setText("");
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(GrantLeavePanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            }else{
                JOptionPane.showMessageDialog(this, "Leave addition failed.");
            }
        
    }//GEN-LAST:event_doneButtonActionPerformed

