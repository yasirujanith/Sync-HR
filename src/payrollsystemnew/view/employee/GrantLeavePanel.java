    private void numOfDaysTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_numOfDaysTextKeyReleased
        Validation.twoIntegers(numOfDaysText);
        if(!"".equals(numOfDaysText.getText())){
            if(leaveTypeCombo.getSelectedItem().toString().equals("Leave")){
                if(Integer.parseInt(numOfDaysText.getText())>Integer.parseInt(remainingLeavesText.getText())){
                    JOptionPane.showMessageDialog(this, "Number exceeding the remaining amount.");
                    numOfDaysText.setText("");
                    numOfDaysText.requestFocus();
                }
            }
            if(leaveTypeCombo.getSelectedItem().toString().equals("No Pay Leave")){
                if(Integer.parseInt(numOfDaysText.getText())>Integer.parseInt(remainingNoPayText.getText())){
                    JOptionPane.showMessageDialog(this, "Number exceeding the remaining amount.");
                    numOfDaysText.setText("");
                    numOfDaysText.requestFocus();
                }
            }
            if(leaveTypeCombo.getSelectedItem().toString().equals("Half Day Leave")){
                if(Integer.parseInt(numOfDaysText.getText())>Integer.parseInt(remainingHalfDayText.getText())){
                    JOptionPane.showMessageDialog(this, "Number exceeding the remaining amount.");
                    numOfDaysText.setText("");
                    numOfDaysText.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_numOfDaysTextKeyReleased


