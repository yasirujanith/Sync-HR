    private void fillemployeeIDCombo() throws ClassNotFoundException, SQLException{
        ArrayList<Employee> employeeList=EmployeeController.getAllEmployees();
        for(Employee employee : employeeList){
            employeeIDCombo.addItem(employee.getEmployeeID());
        }
        AutoCompleteDecorator.decorate(employeeIDCombo);
    }
    
    private void filldeparmentIDCombo() throws ClassNotFoundException, SQLException{
        ArrayList<Department> departmentIDList=DepartmentController.getAllDepartments();
        for(Department dept : departmentIDList){
            departmentIDCombo.addItem(dept.getDepartmentID());
        }
    }
    
    private void fillgradeIDCombo() throws ClassNotFoundException, SQLException{
        ArrayList<Grade> gradeList=GradeController.getAllGrades();
        for(Grade grade : gradeList){
            gradeIDCombo.addItem(grade.getGradeID());
        }
    }

