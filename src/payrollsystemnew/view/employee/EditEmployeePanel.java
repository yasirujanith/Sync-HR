    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        String employeeID=employeeIDCombo.getSelectedItem().toString();
        String firstName=firstNameText.getText();
        String lastName=lastNameText.getText();
        String NIC=nicText.getText();
        String address=addressText.getText();
        String dateOfBirth=dateFormat.format(dobText.getDate());
        String gender=maleButton.isSelected()?"male":"female";
        String maritalStatus=maritalStatusCombo.getSelectedItem().toString();
        String contactNumber=contactNumText.getText();
        String joiningDate=dateFormat.format(joiningDateText.getDate());
        String gradeID=gradeIDCombo.getSelectedItem().toString();
        String departmentID=departmentIDCombo.getSelectedItem().toString();

        try {
            int rowCount1=EmployeeController.updateEmployee(new Employee(employeeID, firstName, lastName, NIC, address, contactNumber, dateOfBirth, gender, maritalStatus, joiningDate, gradeID, departmentID));
            int rowCount2=PaymentController.updatePaymenTypeDetail(new PaymentType(employeeID, cashCheckBox.isSelected(), chequeCheckBox.isSelected(), depositCheckBox.isSelected(), bankNameCombo.getSelectedItem().toString(), accountNumberText.getText()));
            String oldDepartmentID=department.getDepartmentID();
            String oldDepertmentName=department.getName();
            int oldEmployeeCount=department.getNumOfEmployees();
            oldEmployeeCount--;
            int rowCount3=DepartmentController.updateDepartment(new Department(oldDepartmentID, oldDepertmentName, oldEmployeeCount));
            department=DepartmentController.searchDepartment(departmentIDCombo.getSelectedItem().toString());
            String newDepartmentID=department.getDepartmentID();
            String newDepertmentName=department.getName();
            int newEmployeeCount=department.getNumOfEmployees();
            newEmployeeCount++;
            int rowCount4=DepartmentController.updateDepartment(new Department(newDepartmentID, newDepertmentName, newEmployeeCount));
            if(rowCount1>0 && rowCount2>0 && rowCount3>0 && rowCount4>0  ){
                JOptionPane.showMessageDialog(this, "Employee updated succesfully.");
            }else{
                JOptionPane.showMessageDialog(this, "Employee update failed.");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Employee update failed.");
        }
    }//GEN-LAST:event_updateButtonActionPerformed

