    private void employeeIDComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_employeeIDComboActionPerformed
        try {
            empoyeeID=employeeIDCombo.getSelectedItem().toString();
            if(employeeIDCombo.getSelectedIndex()!=-1){
                Employee employee=EmployeeController.searchEmployee(employeeIDCombo.getSelectedItem().toString());
                firstNameText.setText(employee.getFirstName());
                lastNameText.setText(employee.getLastName());
                nicText.setText(employee.getNIC());
                dobText.setSelectedItem(employee.getDateOfBirth());
                if("male".equals(employee.getGender())){
                    maleButton.setSelected(true);
                }
                maritalStatusCombo.setSelectedItem(employee.getMaritalStatus());
                addressText.setText(employee.getAddress());
                contactNumText.setText(employee.getContactNumber());
                joiningDateText.setSelectedItem(employee.getJoiningDate());
                gradeIDCombo.setSelectedItem(employee.getGradeID());
                departmentIDCombo.setSelectedItem(employee.getDepartmentID());
                department = DepartmentController.searchDepartment(employee.getDepartmentID());
                departmentNameText.setText(department.getName());

                PaymentType paymentType=PaymentController.getPaymentTypesDetail(employeeIDCombo.getSelectedItem().toString());
                cashCheckBox.setSelected(paymentType.isCash());
                chequeCheckBox.setSelected(paymentType.isCheque());
                depositCheckBox.setSelected(paymentType.isDeposit());
                bankNameCombo.setSelectedItem(paymentType.getBankName());
                accountNumberText.setText(paymentType.getAccNumber());
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(EditEmployeePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_employeeIDComboActionPerformed


