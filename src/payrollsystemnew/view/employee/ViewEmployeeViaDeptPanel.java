    private void departmentNameComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_departmentNameComboItemStateChanged
        try {
            Department dept=DepartmentController.searchDepartmentByName(departmentNameCombo.getSelectedItem().toString());
            deptIDText.setText(dept.getDepartmentID());
            ArrayList<Employee> employeeList=EmployeeController.getAllEmployeesViaDepartment(dept.getDepartmentID());
            DefaultTableModel dtm=(DefaultTableModel) employeeDetailTable.getModel();
            dtm.setRowCount(0);
            for(Employee emp:employeeList){
                Object[] rowData={emp.getEmployeeID(),emp.getFirstName()+" "+emp.getLastName(),emp.getNIC(),emp.getAddress(),emp.getContactNumber(),emp.getGradeID(),emp.getJoiningDate()};
                dtm.addRow(rowData);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ViewEmployeeViaDeptPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_departmentNameComboItemStateChanged

