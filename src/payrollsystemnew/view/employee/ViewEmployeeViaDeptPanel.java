    private void filldeparmentCombos() throws ClassNotFoundException, SQLException{
        ArrayList<Department> departmentIDList=DepartmentController.getAllDepartments();
        for(Department dept : departmentIDList){
            departmentNameCombo.addItem(dept.getName());
        }
    }
    
    private void printEmployeeTable(){
        HashMap<String,Object> map=new HashMap<String,Object>();
        JRTableModelDataSource dataSource=new JRTableModelDataSource(tableModel);   
        try {
            JasperReport compileReport=JasperCompileManager.compileReport(getClass().getResourceAsStream("EmployeeViaDeptReport.jrxml"));
            map.put("Department", departmentNameCombo.getSelectedItem().toString());
            JasperPrint fillReport=JasperFillManager.fillReport(compileReport, map, dataSource);
            JasperViewer.viewReport(fillReport,false);
        } catch (JRException ex) {
            Logger.getLogger(ViewEmployeeViaDeptPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

