    private void viewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewButtonActionPerformed
        try {
            SimpleDateFormat dateFormat=new SimpleDateFormat("yyyy-MM-dd");
            String employeeID=employeeIDCombo.getSelectedItem().toString();
            String lastDay=getLastDate();
            Calendar c=Calendar.getInstance();
            c.setTime(dateFormat.parse(lastDay));
            c.add(Calendar.MONTH, -1);
            c.add(Calendar.DATE, 1);
            String lastMonthLastDay=dateFormat.format(c.getTime());
            if(PaymentController.isInsentive(employeeID, lastMonthLastDay, lastDay)){
                insentiveBonusText.setText(Double.toString(insentiveBonusAmt));
                //PaymentController.addBonus(new Bonus(employeeID, yearIDCombo.getSelectedItem().toString(), monthIDCombo.getSelectedItem().toString(), "Insentive Bonus", insentiveBonusAmt));
            }else{
                insentiveBonusText.setText("0.0");
            }
            Bonus bonus=PaymentController.searchBonus(employeeID, yearIDCombo.getSelectedItem().toString(), monthNameCombo.getSelectedItem().toString(), "Festival Bonus");
            if(bonus==null){
                festivalBonusText.setText("0.0");
            }else{
                festivalBonusText.setText(Double.toString(bonus.getAmount()));
            }
            bonus=PaymentController.searchBonus(employeeID, yearIDCombo.getSelectedItem().toString(), monthNameCombo.getSelectedItem().toString(), "Other Bonus");
            if(bonus==null){
                otherBonusText.setText("0.0");
            }else{
                otherBonusText.setText(Double.toString(bonus.getAmount()));
            }

            totalBonusText.setText(Double.toString(Double.parseDouble(festivalBonusText.getText())+Double.parseDouble(otherBonusText.getText())+Double.parseDouble(insentiveBonusText.getText())));
        } catch (ParseException | ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ViewEmployeeBonusForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_viewButtonActionPerformed

