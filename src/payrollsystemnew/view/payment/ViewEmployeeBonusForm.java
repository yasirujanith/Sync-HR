    private void fillemployeeIDCombo() throws ClassNotFoundException, SQLException{
        ArrayList<Employee> employeeList=EmployeeController.getAllEmployees();
        for(Employee employee : employeeList){
            employeeIDCombo.addItem(employee.getEmployeeID());
        }
    }
    
    private void fillYearIDCombo() throws ClassNotFoundException, SQLException, ParseException{
        ResultSet yearList=AttendanceController.getAllDates();
        while(yearList.next()){
            yearIDCombo.addItem(yearList.getString(1));
        }
    }
    
    private String getLastDate(){
        String date;
        if(monthNameCombo.getSelectedIndex()<9){
            date=yearIDCombo.getSelectedItem().toString()+"-"+0+(monthNameCombo.getSelectedIndex()+1)+"-"+25;
        }else{
            date=yearIDCombo.getSelectedItem().toString()+"-"+(monthNameCombo.getSelectedIndex()+1)+"-"+25;
        }
        return date;
    }
    
    
