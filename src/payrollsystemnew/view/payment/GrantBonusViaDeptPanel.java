     private void filldeptNameCombo() throws ClassNotFoundException, SQLException{
        ArrayList<Department> deptList=DepartmentController.getAllDepartments();
        for(Department department : deptList){
            deptNameCombo.addItem(department.getName());
        }
    }
    
   private void fillYearIDCombo() throws ClassNotFoundException, SQLException, ParseException{
        ResultSet yearList=AttendanceController.getAllDates();
        while(yearList.next()){
            yearIDCombo.addItem(yearList.getString(1));
        }
    }
    
    private void getLastDate(){
        String date;
        if(monthIDCombo.getSelectedIndex()<9){
            date=yearIDCombo.getSelectedItem().toString()+"-"+0+(monthIDCombo.getSelectedIndex()+1)+"-"+25;
        }else{
            date=yearIDCombo.getSelectedItem().toString()+"-"+(monthIDCombo.getSelectedIndex()+1)+"-"+25;
        }
    }

