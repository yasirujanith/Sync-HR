    private void doneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneButtonActionPerformed
       
        try {
            int rowCount=0;
            Department department=DepartmentController.searchDepartmentByName(deptNameCombo.getSelectedItem().toString());
            ArrayList<Employee> employeeList=EmployeeController.getAllEmployeesViaDepartment(department.getDepartmentID());
            if(!"".equals(festivalBonusText.getText())){
                for(Employee employee : employeeList){
                    rowCount=PaymentController.addBonus(new Bonus(employee.getEmployeeID(), yearIDCombo.getSelectedItem().toString(), monthIDCombo.getSelectedItem().toString(), "Festival Bonus", Double.parseDouble(festivalBonusText.getText())));
                }
                if(rowCount>0){
                    JOptionPane.showMessageDialog(this, "Festival Bonus added successfully.");
                }else{
                    JOptionPane.showMessageDialog(this, "Festival Bonus addition failed.");
                }
            }
            if(!"".equals(otherBonusText.getText())){
                for(Employee employee : employeeList){
                    rowCount=PaymentController.addBonus(new Bonus(employee.getEmployeeID(), yearIDCombo.getSelectedItem().toString(), monthIDCombo.getSelectedItem().toString(), "Other Bonus", Double.parseDouble(otherBonusText.getText())));
                }
                if(rowCount>0){
                    JOptionPane.showMessageDialog(this, "Other Bonus added successfully.");
                }else{
                    JOptionPane.showMessageDialog(this, "Other Bonus addition failed.");
                }
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(GrantBonusViaDeptPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_doneButtonActionPerformed

