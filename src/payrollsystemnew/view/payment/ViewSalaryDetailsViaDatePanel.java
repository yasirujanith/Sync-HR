    private void processButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processButtonActionPerformed
        try {
            double total=0;
            DefaultTableModel tableModel=(DefaultTableModel) salaryTable.getModel();
            tableModel.setRowCount(0);
            ResultSet rst=PaymentController.getPaidSalaryDetailViaDate(getLastDate());
            while(rst.next()){
                Employee employee=EmployeeController.searchEmployee(rst.getString(6));
                Object[] rowData={employee.getEmployeeID(),employee.getFirstName()+" "+employee.getLastName(),rst.getString(4)};
                tableModel.addRow(rowData);
                total+=rst.getDouble(4);
            }
            totalText.setText(Double.toString(total));
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ViewSalaryDetailsViaDatePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_processButtonActionPerformed


