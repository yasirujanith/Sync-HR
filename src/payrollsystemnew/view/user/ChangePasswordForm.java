    private void currentPasswordFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_currentPasswordFieldKeyReleased
        try {
            User user=UserController.searchUserByName(nameLable.getText());
            if(currentPasswordField.getText().equals(user.getPassword())){
                validationLabel.setForeground(Color.LIGHT_GRAY);
                validationLabel.setText("Valid.");
            }else{
                validationLabel.setForeground(Color.red);
                validationLabel.setText("Invalid.");
                changeButton.setEnabled(false);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ChangePasswordForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_currentPasswordFieldKeyReleased

    private void confirmNewPasswordFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_confirmNewPasswordFieldKeyReleased
        if(newPasswordField.getText().equals(confirmNewPasswordField.getText())){
            if(!"".equals(newPasswordField.getText())){
                validationLabel.setForeground(Color.LIGHT_GRAY);
                validationLabel.setText("Match.");
                changeButton.setEnabled(true);
            }else{
                validationLabel.setForeground(Color.red);
                validationLabel.setText("Fill All Fields.");
                changeButton.setEnabled(false);
            }
        }else{
            validationLabel.setForeground(Color.red);
            validationLabel.setText("Do not Match.");
            changeButton.setEnabled(false);
        }
    }//GEN-LAST:event_confirmNewPasswordFieldKeyReleased

