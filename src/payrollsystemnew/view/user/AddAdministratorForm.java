    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        try {
            String userName=nameLable.getText();
            int rowCount=UserController.addUser(new User(userName, passwordField.getText()));
            if(rowCount>0){
                UserController.addUserLogin(userName);
                JOptionPane.showMessageDialog(this, "User added successfully.");
            }else{
                JOptionPane.showMessageDialog(this, "User addition failed.");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddAdministratorForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        new PostLoadingForm().setVisible(true);
        this.dispose();

    }//GEN-LAST:event_addButtonActionPerformed

    private void confirmPasswordFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_confirmPasswordFieldKeyReleased
        
            if(passwordField.getText().equals(confirmPasswordField.getText())){
                validationLabel.setForeground(Color.GRAY);
                validationLabel.setText("Match.");
                addButton.setEnabled(true);
            }else{
                validationLabel.setForeground(Color.RED);
                validationLabel.setText("Do not Match.");
                addButton.setEnabled(false);
            }
        
    }//GEN-LAST:event_confirmPasswordFieldKeyReleased

